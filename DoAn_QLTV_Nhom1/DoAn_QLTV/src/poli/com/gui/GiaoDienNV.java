    private void ccb_TK_tinhTrangItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_ccb_TK_tinhTrangItemStateChanged
        if(ccb_TK_tinhTrang.getSelectedIndex()==-1)
        {
            return;
        }
        txtTimKiemTTPM.setText("");
        if(ccb_TK_tinhTrang.getSelectedItem().equals("Tất cả"))
        {
            poli.com.dao.muonTraDao.createDesignedTable(poli.com.dao.muonTraDao.hienThi_ALL(), jtbPhieuMuon);
            return;
        }

        if(ccb_TK_tinhTrang.getSelectedItem().equals("Theo tuần"))
        {
            poli.com.dao.muonTraDao.createDesignedTable(poli.com.dao.muonTraDao.thongKeDsMuonTheoTuan(), jtbPhieuMuon);
            return;
        }
        else
        {
            poli.com.dao.muonTraDao.createDesignedTable(poli.com.dao.muonTraDao.timkiemTT((String)ccb_TK_tinhTrang.getSelectedItem()), jtbPhieuMuon);
            return;
        }
    }//GEN-LAST:event_ccb_TK_tinhTrangItemStateChanged

