 void loadTieuDe()
    {
         Dimension panelSize = jpnTieuDe.getSize();

        // Lấy kích thước của label
        Dimension labelSize = jlbTieuDe.getPreferredSize();

        // Tính toán vị trí của label để đặt nó vào giữa panel
        int x = (panelSize.width - labelSize.width) / 2;
        int y = (panelSize.height - labelSize.height) / 2;

        // Đặt vị trí của label
        jlbTieuDe.setBounds(x, y, labelSize.width, labelSize.height);
        
        //Sách
       
        Timer timer = new Timer(100,new ActionListener() {
             int x = 0;
              long lastColorChangeTime = System.currentTimeMillis();
            @Override
            public void actionPerformed(ActionEvent e) {
              x += 5;
                jlbTieuDe.setLocation(x, y);

                // Thay đổi màu của label sau mỗi 1000ms (1 giây)
                long currentTime = System.currentTimeMillis();
                if (currentTime - lastColorChangeTime >= 1000) {
                    if (jlbTieuDe.getForeground() == Color.RED) {
                        jlbTieuDe.setForeground(Color.BLUE);
                    } else {
                        jlbTieuDe.setForeground(Color.RED);
                    }
                    lastColorChangeTime = currentTime;
                }

                // Nếu label đã di chuyển ra khỏi khung hình, thiết lập lại vị trí
                if (x >= jpnTieuDe.getWidth()) {
                    x = -jlbTieuDe.getWidth();
                }
            }
        });
        timer.start();
    }
