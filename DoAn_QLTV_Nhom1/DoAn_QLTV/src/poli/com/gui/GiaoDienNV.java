    private void btnXoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnXoaMouseClicked
        int index = tbDSCam.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn 1 độc giả trong bảng để xóa", "Thông Báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String maDG = tbDSCam.getValueAt(index, 0).toString();
        int confirmResult = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa độc giả này không?", "Xác Nhận Xóa", JOptionPane.YES_NO_OPTION);
        if (confirmResult == JOptionPane.YES_OPTION) {
            if (poli.com.dao.dsCamDao.XoaDocGiaKhoiDsCam(maDG)) {
                JOptionPane.showMessageDialog(this, "Xóa độc giả thành công", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
                   load_ds_cam();
            } else {
                JOptionPane.showMessageDialog(this, "Lỗi hệ thống", "Thông Báo", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnXoaMouseClicked

