    private void btThemDGMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btThemDGMouseClicked
         
        if(kiemTraLoi() == false){
            return;
        }
        boolean kt = poli.com.dao.docGiaDao.check_TrungSoThe(cb_Sothe.getSelectedItem().toString());
        if(kt == true){
            JOptionPane.showMessageDialog(this, "Trùng số thẻ", "Thông Báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        moli.com.modal.docGia dg = new moli.com.modal.docGia();
        dg.setTenDG(txtTenDG.getText());
        if(jrbNamDG.isSelected()){
            dg.setGioiTinh("Nam");
        }else{
            dg.setGioiTinh("Nữ");
        }

        java.sql.Date ngaySinh = new java.sql.Date(jdcNgaySinhDG.getDate().getTime());
        dg.setNgaySinh(ngaySinh);
        dg.setDiaChi(txtDiaChiDG.getText());
        dg.setSoDT(txtSDTDG.getText());
        if(cb_Sothe.getSelectedIndex()== -1){
            //thông báo lỗi
        }
        dg.setSoThe(cb_Sothe.getSelectedItem().toString());

        if (poli.com.dao.docGiaDao.themDocGia(dg.getTenDG(), dg.getGioiTinh(), dg.getNgaySinh(), dg.getDiaChi(), dg.getSoDT(), dg.getSoThe()) > 0) {
            JOptionPane.showMessageDialog(null, "Thêm thành công");
            loadTTDG();
        } else {
            JOptionPane.showMessageDialog(null, "Thêm thất bại");
        }
    }//GEN-LAST:event_btThemDGMouseClicked

