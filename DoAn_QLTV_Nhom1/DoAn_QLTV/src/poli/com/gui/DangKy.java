    boolean kiemTraTT()
    {
         char[] passwordChars = txtMatKhau.getPassword();
    String password = new String(passwordChars);
    char[] passwordChars1 = txtNhapLaiMK.getPassword();
    String password1 = new String(passwordChars1);

    if(txtMaNhanVien.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập mã nhân viên.", "Thông Báo", JOptionPane.WARNING_MESSAGE);
        txtMaNhanVien.requestFocus();
        return false;
    }

    if(password.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập mật khẩu.", "Thông Báo", JOptionPane.WARNING_MESSAGE);
        txtMatKhau.requestFocus();
       return false;
    }

    if(password1.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập lại mật khẩu.", "Thông Báo", JOptionPane.WARNING_MESSAGE);
        txtNhapLaiMK.requestFocus();
       return false;
    }

    if(!password.equals(password1)) {
        JOptionPane.showMessageDialog(this, "Mật khẩu không trùng khớp, vui lòng nhập lại.", "Thông Báo", JOptionPane.WARNING_MESSAGE);
        txtMatKhau.requestFocus();
        return false;
    }
    return true;
    }

