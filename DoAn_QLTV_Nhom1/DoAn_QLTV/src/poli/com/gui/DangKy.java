    private void btDangKyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDangKyActionPerformed
       boolean check  = kiemTraTT();
       if(check == false)
       {
           return;
       }
        boolean checkTT = poli.com.dao.taiKhoanDao.kiemTraKT(txtMaNhanVien.getText().toString());
        boolean kiemTraNV = poli.com.dao.nhanVienDao.check_maNV(txtMaNhanVien.getText().toString());
       if(kiemTraNV == true)
       {
            if(checkTT==true)
        {
             JOptionPane.showMessageDialog(this, "Mã nhân viên này đã đăng kí tài khoản rồi.", "Thông Báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        else
        {
            int quyen = poli.com.dao.nhanVienDao.check_Quyen_maNV(txtMaNhanVien.getText().toString()) ? 1 : 0;
             char[] passwordChars = txtMatKhau.getPassword();
            String password = new String(passwordChars);
            boolean checkDK = poli.com.dao.taiKhoanDao.themTK(txtMaNhanVien.getText().toString(), password, quyen);
            if(checkDK == true)
            {
                 JOptionPane.showMessageDialog(null, "Đăng kí thành công !!!", "Chúc mừng", JOptionPane.INFORMATION_MESSAGE);
                 clearData();
                return;
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Đăng kí thất bại.", "Thông Báo", JOptionPane.WARNING_MESSAGE);
                return;
            }
        }
       }
       else
       {
           JOptionPane.showMessageDialog(this, "Nhân viên này không tồn tại.", "Thông Báo", JOptionPane.WARNING_MESSAGE);
           return;
       }
    }//GEN-LAST:event_btDangKyActionPerformed
